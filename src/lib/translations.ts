export const translateDynamic = (text: string, lang: Language): string => {
  if (!text) return text;
  if (lang === "en") return text;
  const dict = dynamicTranslations[lang];
  if (dict && dict[text]) {
    return dict[text];
  }
  // Try case-insensitive matching
  if (dict) {
    const key = Object.keys(dict).find(k => k.toLowerCase() === text.toLowerCase());
    if (key) return dict[key];
  }
  return text;
};
